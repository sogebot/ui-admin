<template>
  <v-dialog
    v-model="dialogController"
    persistent
    :fullscreen="$vuetify.breakpoint.mobile"
    transition="dialog-bottom-transition"
    max-width="600"
  >
    <v-card :loading="!isLoaded">
      <v-toolbar dense color="dark">
        <v-btn
          icon
          @click="dialogController = false"
        >
          <v-icon>{{ mdiClose }}</v-icon>
        </v-btn>
        <span class="headline">Edit custom URLs</span>
        <v-spacer />
        <v-btn
          small
          :text="!$vuetify.breakpoint.xs"
          :icon="$vuetify.breakpoint.xs"
          :loading="isSaving"
          @click="save"
        >
          <v-icon class="d-flex d-sm-none">
            {{ mdiFloppy }}
          </v-icon>
          <span class="d-none d-sm-flex">{{ translate('dialog.buttons.saveChanges.idle') }}</span>
        </v-btn>
      </v-toolbar>
      <v-card-text class="pa-0">
        <v-alert
          border="left"
          color="red"
          :icon="mdiExclamationThick"
          text
          type="success"
        >
          Not all URLs will be working! e.g twitter.com, reddit.com have forbidden iframes.
        </v-alert>
        <v-list>
          <transition-group name="fade">
            <v-list-item v-for="item of items" :key="item.id">
              <v-text-field
                v-model="item.name"
                label="Name"
              />
              <v-text-field
                v-model="item.url"
                label="URL"
              />
              <v-btn color="error" icon @click="rmItem(item.id)">
                <v-icon>{{ mdiDelete }}</v-icon>
              </v-btn>
            </v-list-item>
          </transition-group>
        </v-list>
      </v-card-text>
      <v-card-actions>
        <v-btn
          text
          @click="addItem"
        >
          Add new item
        </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script lang="ts">
import {
  mdiClose, mdiDelete, mdiExclamationThick, mdiFloppy,
} from '@mdi/js';
import {
  defineComponent, onMounted, ref, useContext, watch,
} from '@nuxtjs/composition-api';
import translate from '@sogebot/ui-helpers/translate';
import { v4 } from 'uuid';

import { error } from '../../../functions/error';

import { WidgetCustomInterface } from '~/.bot/src/database/entity/widget';
import api from '~/functions/api';

type Props = {
  dialog: boolean,
};

export default defineComponent({
  props: { dialog: Boolean },
  setup (props: Props, ctx) {
    const { $axios } = useContext();
    const dialogController = ref(props.dialog);
    const isLoaded = ref(false);
    const isSaving = ref(false);
    const items = ref([] as Pick<WidgetCustomInterface, 'id' | 'url' | 'name'>[]);
    const valid = ref(true);
    const markedToDelete = ref([] as string[]);

    watch(dialogController, (val) => {
      ctx.emit('update:dialog', val);
    });

    const refresh = () => {
      api.gql<{ widgetCustomGet: typeof items.value }>($axios, '{ widgetCustomGet { id url name } }').then((data) => {
        items.value = data.widgetCustomGet;
        isLoaded.value = true;
      });
    };

    const save = async () => {
      isSaving.value = true;
      await Promise.all(
        markedToDelete.value.map((id) => {
          return new Promise((resolve) => {
            const query = `mutation widgetCustomRemove($id: String!) { widgetCustomRemove(id: $id) }`;
            api.gql<any>($axios, query, { id }).catch(error).finally(() => resolve(true));
          });
        }),
      );
      await Promise.all(
        items.value.map((item) => {
          return new Promise((resolve) => {
            const query = `mutation widgetCustomSet($id: String!, $data: WidgetCustomInput!) {
              widgetCustomSet(id: $id, data: $data) {
                id
              }
            }`;

            const { id, ...data } = item;
            api.gql<any>($axios, query, {
              id,
              data,
            }).then(() => ctx.emit('save')).catch(error).finally(() => resolve(true));
          });
        }),
      );
      markedToDelete.value = [];
      isSaving.value = false;
      dialogController.value = false;
    };

    const addItem = () => {
      items.value.push({
        id:   v4(),
        name: '',
        url:  '',
      });
    };

    const rmItem = (id: string) => {
      items.value = items.value.filter(o => o.id !== id);
      markedToDelete.value.push(id);
    };

    onMounted(() => {
      refresh();
    });

    return {
      // refs
      dialogController,
      isLoaded,
      isSaving,
      items,
      valid,

      // functions
      translate,
      save,
      addItem,
      rmItem,

      // icons
      mdiClose,
      mdiDelete,
      mdiExclamationThick,
      mdiFloppy,
    };
  },
});
</script>
